// here i will talk about datatypes in cpp
#include <iostream>
using namespace std;
int main()
{
    // integer type
    int integer = 9;
    cout << "The value of integer is " << integer << endl;
    // floating point
    float floating = 9.555;
    cout << "The Value of float is " << floating << endl;
    // String type
    string sentence = "this is a datatype";
    cout << "The string is : " << sentence << endl;
}